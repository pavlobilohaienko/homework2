import Foundation

// let - константа
// var - переменные

// Явное определение типа
let pineapples: Int = 5
// Неявное определение типа
var apples = 5

// Мы можем изменять значение переменных позже
apples = 4

// Мы не можем менять значения констант после установки их начального значения
// pineapples = 1

